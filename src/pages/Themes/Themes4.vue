В соответствии с общим стилем пользовательского интерфейса CorelDRAW на кнопке инструмента (Кривая )отображается значок последнего из использованных инструментов, кнопки которых присутствуют на его панели, и щелчок на этой кнопке снова активизирует тот же инструмент.<br />
