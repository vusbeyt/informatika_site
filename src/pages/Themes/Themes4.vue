Использование вспомогательных объектов. В компьютерной графике широко используется метод привязывания объектов к некоторым вспомогательным элементам или друг к другу, т.е. форсируется выравнивание узлов объектов при рисовании или перемещении. CorelDRAW, конечно, не стал исключением из правила, и предоставляет широкий спектр возможностей по использованию привязок.<br />
Привязка может осуществляться к вспомогательным объектам (сетке, направляющим линиям), а также к другим объектам. В версии CorelDRAW 12 появились вспомогательные объекты, называемые динамическими направляющими, а также расширились возможности по использованию привязок объектов друг к другу. Кроме того, при рисовании и перемещении объектов часто используются измерительные линейки.<br />
Измерительные линейки. Измерительные линейки активизируются посредством выбора команды меню View/Rulers (Вид/Линейки). Линейки располагаются в верхней и в левой частях рабочего окна и позволяют определить размеры и местонахождение объектов. Единицы измерения на линейках соответствуют единицам измерения, принятым в редактируемом документе. При выбранном инструменте Pick Tool (Стрелка) и при отсутствии выделенных объектов можно установить единицы измерения с помощью выпадающего списка Units (Единицы) панели Property Bar (Панель свойств).<br />
Нулевые координаты на линейках имеет левый нижний угол рабочей области, однако при необходимости точку отсчета можно изменить, перетащив мышью кнопку, расположенную в месте пересечения линеек.<br />
Сетка. Сетка — это вспомогательный объект, представляющий собой множество горизонтальных и вертикальных линий, расположенных друг от друга на заданном расстоянии. Привязка осуществляется к узлам сетки, т.е. к точкам, лежащим на пересечении линий сетки (рис.2). <br />
<img src="img/img-159.png" ><br />
Для того, чтобы настроить сетку, следует выбрать команду меню View/Grid and Ruler Setup (Вид/Настройка сетки и линеек). В появившемся диалоговом окне можно установить следующие параметры настройки сетки:<br />
- Frequency (Частота) — устанавливает частоту горизонтальных и вертикальных линий сетки, т.е. количество линий сетки на единицу измерения;<br />
- Spacing (Интервал) — устанавливает расстояние между линиями сетки в единицах измерения;<br />
- Show Grid (Показать сетку) — устанавливает, будет ли отображаться сетка;<br />
- Snap To Grid (Привязка к сетке) — устанавливает, будет ли осуществляться привязка объектов к узлам сетки;<br />
- Show grid as lines/ Show grid as dots (Показывать сетку как линии/Показывать сетку как точки) — устанавливает режим отображения сетки в виде линий или в виде точек соответственно.<br />
Направляющие. Направляющие линии — это вспомогательные линии, которые используются для точного позиционирования объектов (рис.3). Самый быстрый и простой способ получить направляющие — щелкнуть мышью на одной из измерительных линеек и перетащить курсор мыши в рабочую область.<br />
<img src="img/img-160.png" ><br />
Использование привязки к направляющим для построения трапеции с помощью инструмента Pen Tool (Перо)<br />
Если нужно добавить сразу несколько направляющих, проходящих под определенным углом через определенные координаты, можно воспользоваться командой меню View/Guidelines Setup (Вид/Настройка направляющих). В появившемся диалоговом окне в иерархическом списке слева можно выбрать, какие направляющие будут настраиваться: горизонтальные, вертикальные, наклонные или заготовки.<br />
При выборе пункта Horizontal (Горизонтальные) активизируется панель со списком координат горизонтальных направляющих. В поле над списком можно ввести другие координаты, а в выпадающем списке справа от поля — выбрать единицы измерения. Кроме того, имеются 4 кнопки:<br />
- Add (Добавить) — добавляет направляющую с координатой, заданной в поле над списком направляющих;<br />
- Move (Переместить) — переносит активную направляющую в координату, введенную в поле над списком направляющих;<br />
- Delete (Удалить) — удаляет направляющую из списка;<br />
- Clear (Очистить) — очищает список направляющих.<br />
При выборе пункта Vertical (Вертикальные) активизируется панель, сходная с панелью настройки горизонтальных направляющих.<br />
При выборе пункта Guides (Направляющие) на панели отобразится список всех направляющих, причем горизонтальные направляющие будут помечены буквой H, а вертикальные — буквой V. Кроме того, с помощью этой панели можно создавать наклонные направляющие. Для этого в списке Specify (Определить) выбирается метод создания наклонной направляющей: Angle and 1 Point (Угол и точка) или 2 Points (Две точки). В первом случае направляющая будет задаваться координатами точки (X;Y) и углом, под которым она проходит через эту точку, а во втором — координатами двух точек.<br />
Привязка объектов. В CorelDRAW существует возможность устанавливать привязку объектов к сетке, направляющим и другим объектам, которая будет действовать при создании и трансформации объектов. Активизация привязок осуществляется с помощью соответствующих команд меню:<br />
- View/Snap To Grid (Вид/Привязать к сетке) — устанавливает привязку к узлам сетки;<br />
- View/Snap To Guidelines (Вид/Привязать к направляющим) — устанавливает привязку к направляющим;<br />
- View/Snap To Objects (Вид/Привязать к объектам) — устанавливает привязку объектов друг к другу.<br />
У каждого объекта в CorelDRAW есть габаритные элементы, которые подчиняются установленным привязкам, если попадают в область, которая называется областью фиксации. Чтобы указать, какими габаритными элементами объекты привязываются друг к другу, следует выбрать команду меню View/Snap To Objects Setup (Вид/Настройка привязки к объектам). Появится диалоговое окно, в котором с помощью соответствующих флажков можно установить, к каким элементам будут привязываться объекты:<br />
- Node (Узел) — устанавливает привязку к узлам объектов;<br />
- Intersection (Пересечение) — устанавливает привязку к геометрическим пересечениям объектов;<br />
- Midpoint (Середина) — устанавливает привязку к серединам сегментов;<br />
- Quadrant (Квадрант) — устанавливает привязку к точкам, которые соответствуют углам в 0°, 90°, 180° и 270° на окружности, эллипсе или дуге;<br />
- Tangent (Касательная) — устанавливает привязку к точкам на контуре дуги, окружности или эллипса, в которых линия касается контура, но не пересекает его;<br />
- Perpendicular (Перпендикуляр) — устанавливает привязку к точкам на сегменте, в которых линия перпендикулярна сегмент;<br />
- Edge (Край) — устанавливает привязку к точкам на контуре объекта;<br />
- Center (Центр) — устанавливает привязку к центру габаритного контейнера объекта;<br />
- Text baseline (Базовая линия текста) — устанавливает привязку к точкам на базовой линии художественного или простого текста.<br />
