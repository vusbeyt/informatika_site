                <strong><strong class="name">ОСНОВЫ РАБОТЫ</strong><br />
Работа над любой иллюстрацией заключается в создании объектов, их редактировании и задании их правильного взаимного расположения. Исходя из этого, основными принципами работы в CorelDRAW можно назвать следующие.<br />
- Создание простых геометрических фигур или произвольных кривых и ломаных, как замкнутых, так и разомкнутых. Вставка и форматирование текста.<br />
- Редактирование любого объекта, изменение цвета контура и заливки, изменение формы объекта.<br />
- Вставка готовых картинок или ранее созданных иллюстраций в документ.<br />
- Применение разнообразных художественных эффектов.<br />
- Точное позиционирование объектов относительно страницы и друг друга.<br />
Элементы рабочего окна редактора. При первом запуске программы CorelDRAW на экране появится диалоговое окно, в котором можно выбрать режим работы. Если флажок Show this Welcome Screen at startup (Показывать это окно при запуске) оставить установленным, то это диалоговое появится и при следующем запуске программы. Чтобы начать работу над новым рисунком, необходимо выбрать вариант New Graphic (Новый рисунок).<br />
Интерфейс рабочего окна программы, как и интерфейс большинства приложений, предназначенных для работы в ОС Windows, содержит в первую очередь стандартные элементы:<br />
1-заголовок окна, 2-строка меню, 3-стандартная панель инструментов,<br> 4-панель свойств или атрибутов, 5 -панель инструментов, 6 - палитра цветов,<br />
7- печатная страница, 8- рабочий стол, 9- горизонтальные и вертикальные полосы прокрутки, 10- элементы управления для перехода по страницам,<br> 11- горизонтальная и вертикальная линейка, 12- пристыковываемые окна, 13- строка состояния.<br />
<img src="img/img-137.png" ><br />
Главное меню обеспечивает доступ к командам CorelDRAW, объединенным в следующие группы:<br />
- File (Файл) — команды, позволяющие открывать, сохранять, импортировать, экспортировать файлы, распечатывать рисунки, публиковать их в Internet; в нижней части списка команды расположен список последних открывавшихся файлов;<br />
- Edit (Правка) — команды, позволяющие отменять и повторять последние действия, копировать объекты, настраивать их свойства;<br />
- View (Вид) — команды, позволяющие настраивать окно отображения объектов, устанавливать и настраивать вспомогательные элементы окна;<br />
- Layout (Макет) — команды, позволяющие добавлять, удалять и переименовывать страницы графического документа и настраивать их свойства;<br />
- Arrange (Упорядочить) — команды, позволяющие активизировать инструменты преобразования и выравнивания объектов;<br />
- Effects (Эффекты) — команды, позволяющие добавить различные специальные эффекты к объектам (например, эффекты выдавливания, тени, прозрачности и т.п.);<br />
- Bitmaps (Растры) — команды, позволяющие преобразовывать векторные изображения в растровые и наоборот, редактировать растровые изображения и добавлять к ним различные эффекты;<br />
- Text (Текст) — команды для работы с текстовыми объектами;<br />
- Tools (Инструменты) — команды, позволяющие настраивать интерфейс программы, включать и отключать панели инструментов, а также настраивать их;<br />
- Window (Окно) — команды, позволяющие переключаться между окнами разных файлов, одновременно открытых в CorelDRAW, а также вызывать дополнительные окна палитр, докеров и панелей инструментов;<br />
- Help (Помощь) — команды, позволяющие обратиться к встроенной справочной системе CorelDRAW.<br />
<img src="img/img-138.png" ><br />
В центре окна программы расположена рабочая область программы, изображенная в виде листа бумаги. При выводе изображения на печать будет напечатано только то, что находится внутри рабочей области. Полосы прокрутки позволяют передвигаться по изображению, а измерительные линейки — точно позиционировать элементы рисунка и измерять их размеры. Для работы с цветом в правой части окна расположена палитра цветов. В строке состояния выводится дополнительная информация об объектах, существенно облегчающая работу с редактором.<br />
Помимо стандартной панели инструментов, в CorelDRAW имеются и специфические панели, из которых особенно важными являются панель Toolbox (Инструменты) Рис.1.1 и панель Property Bar (Панель свойств). Инструменты панели Toolbox (Инструменты) позволяют создавать объекты, а элементы панели Property Bar (Панель свойств) позволяют изменять их свойства.<br />
Чтобы расположить панель инструментов в произвольном месте, надо перетащить ее с помощью мыши. Панели можно закрепить с любой стороны окна программы или расположить в произвольном месте рабочего окна. Главное меню также является панелью, и его местоположение также можно изменить.<br />
Любую панель инструментов можно отображать или не отображать в рабочем окне. Для этого достаточно выбрать команду меню Window/Toolbars (Окно/Панели инструментов) и установить соответствующие флажки: для стандартной панели — Standard (Основная), для главного меню программы — Menu Bar (Панель меню), для панели свойств — Property Bar (Панель свойств), для панели инструментов — Toolbox (Инструменты) и для строки состояния — Status Bar (Строка состояния).<br />
Понятие объекта в CorelDRAW. Любое изображение в векторном формате состоит из множества составляющих частей — объектов, которые можно редактировать независимо друг от друга. Вне зависимости от внешнего вида, векторные объекты CorelDRAW имеют ряд общих характеристик (рис.1.2).<br />
<img src="img/img-139.png" ><br />
Любой объект имеет некоторое количество точек, или узлов, соединенных прямыми или кривыми линиями — сегментами. Координаты узлов и параметры сегментов определяют внешний вид объекта. Сегменты объекта образуют контур. Различают замкнутые и разомкнутые контуры. Область, ограниченную замкнутым контуром, принято называть заливкой. Эту область можно закрасить (или залить) одним цветом, смесью цветов, узором или текстурой. У одного объекта не может быть несколько разных заливок или соединительных линий разной толщины и разных цветов.<br /><br><br />
<b>ПРОСТЫЕ ФИГУРЫ</b><br />
К ним можно отнести прямоугольники и эллипсы, многоугольники, клетки и спирали.<br />
<img src="img/img-140.png" ><br />
Прямоугольник. Для того чтобы изобразить прямоугольник, необходимо выбрать инструмент Rectangle Tool (Прямоугольник) в панели инструментов Toolbox (Графика), расположенной в левой части окна программы. Изображение кнопки при этом изменится, она будет как бы зафиксирована в нажатом состоянии. Это говорит о включении режима создания прямоугольников. Она останется нажатой, пока не будет выбран другой инструмент.<br />
Далее необходимо установить указатель мыши в любом месте рабочей области, нажать левую кнопку мыши, и, не отпуская ее, передвигать мышь. На экране появится прямоугольник, размеры которого будут меняться вместе с передвижением мыши. Чтобы закончить рисование, нужно отпустить кнопку мыши — прямоугольник останется на экране; вокруг него появится восемь габаритных маркеров (черные прямоугольники), в центре — перекрестие, а в вершинах — узлы.<br />
Если при рисовании прямоугольника удерживать нажатой клавишу [Ctrl], то будет рисоваться квадрат. Кроме того, в CorelDraw предусмотрена возможность рисовать объекты не от угла габаритного контейнера, а от центра объекта. Для этого нужно удерживать нажатой клавишу [Shift]. Причем, если требуется нарисовать квадрат от центра к углам, то следует одновременно удерживать нажатыми клавиши [Ctrl] и [Shift].<br />
Кроме того, существует быстрый способ нарисовать прямоугольник с размерами, совпадающими с размерами страницы. Для этого нужно дважды щелкнуть на инструменте Rectangle Tool (Прямоугольник).<br />
Начиная с версии CorelDRAW 11 появилась дополнительная возможность рисования прямоугольников. Если раскрыть вспомогательную панель инструмента Rectangle Tool (Прямоугольник), то можно выбрать инструмент 3 Point Rectangle Tool (Трехточечный прямоугольник), позволяющий нарисовать прямоугольник следующим образом: щелкнуть мышью, не отпуская кнопку мыши перетащить ее и щелкнуть еще раз — будет зафиксирована одна из сторон прямоугольника, после чего перетаскиванием мыши и третьим щелчком следует достроить прямоугольник.<br />
Эллипс. Для рисования эллипсов предназначен инструмент Ellipse Tool (Эллипс) на панели инструментов Toolbox (Инструменты). При рисовании эллипсов используются те же приемы, что и при рисовании прямоугольников, и использование клавиш [Ctrl] и [Shift] так же позволяет рисовать эллипсы от центра и круги. Кроме того, с версии v11 для рисования эллипсов появился новый инструмент, аналогичный инструменту 3 Point Rectangle (Трехточечный прямоугольник) — инструмент 3 Point Ellipse (Трехточечный эллипс): первые два щелчка задают один из диаметров эллипса, затем перетягиванием мыши и третьим щелчком достраивается эллипс.<br />
 Многоугольники, спирали, клетки. Инструменты для рисования многоугольников, спиралей и клеток объединены на одной вспомогательной панели <br />
<img src="img/img-141.png" ><br />
Для рисования многоугольников используется инструмент Polygon Tool (Многоугольник) панели Toolbox (Инструменты). При этом как до, так и после рисования многоугольника можно выполнить настройку его параметров: установить количество вершин и тип фигуры (многоугольник или звезда). Для звезд с числом вершин 7 и более можно с помощью ползунка Sharpness of Polygon (Остроконечность многоугольника) на панели Property Bar (Панель свойств) установить степень заострения (рис.2).<br />
<img src="img/img-142.png" ><br />
Чтобы выбрать инструмент для рисования спиралей, необходимо нажать кнопку Spiral Tool (Спираль). При этом с помощью панели Property Bar (Панель свойств) необходимо непосредственно перед рисованием установить параметры спирали: количество витков спирали и принцип рисования витков (т.е. будут ли расстояния между витками равными или же они будут задаваться по логарифмическому принципу). Изменение этих параметров после рисования не приведет к изменению выделенной спирали.<br />
Чтобы создать клетку, можно воспользоваться инструментом Graph Paper Tool (Клетка) на панели Toolbox (Инструменты). Параметры клетки — количество ячеек по горизонтали и по вертикали — также требуется установить перед ее созданием, используя панель Property Bar (Панель свойств).<br />
<b>ПРАВКА ОБЪЕКТОВ</b><br />
Выделение объектов. Как уже отмечалось, при создании объектов вокруг них появляются черные прямоугольники, называемые маркерами выделения. То есть, вновь созданный объект уже выделен, и вы можете сразу приступать к его редактированию. Помимо этого можно выделять любые другие объекты.<br />
<img src="img/img-143.png" ><br />
Для выделения объектов служит инструмент Pick Tool (Стрелка): если этот инструмент выбран, достаточно просто щелкнуть на объекте, чтобы выделить его. Чтобы отменить выделение объектов, надо щелкнуть мышью за пределами области выделения. Кроме того, когда выбран инструмент Pick Tool (Стрелка) и не выделен ни один объект, в панели Property Bar (Панель свойств) видна кнопка Treat as Filled (Использовать как заливку). Если она нажата, то выделять объекты можно с помощью щелчка в любом месте объекта, а если кнопка не нажата, то объекты выделяются только после щелчка на контуре.<br />
Иногда бывает необходимо выделить все объекты сразу. Для этого достаточно дважды щелкнуть мышью на инструменте Pick Tool (Стрелка). Выделение нескольких объектов обозначается маркерами выделения, расположенными вокруг них (рис.1).<br />
Для того чтобы выделить несколько произвольных объектов, следует сначала выделить один их них, а затем, удерживая нажатой клавишу [Shift], выделить остальные объекты, щелкая на них последовательно мышью. При этом в строке состояния выводится информация о выделенных объектах, например, 3 Objects selected (Выделено 3 объекта). Чтобы снять выделение одного из множества выделенных объектов, достаточно щелкнуть на нем, удерживая клавишу [Shift]. При этом все остальные объекты останутся выделенными.<br />
 Еще один способ выделения объектов — с помощью рамки выделения. Для этого надо щелкнуть левой кнопкой мыши выше и левее объекта или объектов, которые вы хотите выделить, и, передвигать мышь, удерживая ее нажатой. На экране между начальной точкой и текущим местом указателя появится пунктирный прямоугольник, называемый рамкой выделения. После того, как нужные объекты окажутся внутри пунктирного прямоугольника, необходимо отпустить кнопку мыши — объекты, целиком оказавшиеся внутри пунктирного прямоугольника, будут выделены (рис.2)<br />
<img src="img/img-144.png" ><br />
Если во время выделения рамкой держать нажатой клавишу [Alt], то выделены будут не только объекты, целиком оказавшиеся внутри пунктирного прямоугольника, но и частично попавшие в него. После выделения нескольких объектов, с ними можно работать как с единым объектом.<br />
 Отмена и возврат последних действий. В CorelDRAW, как и во многих других приложениях Windows, можно отменять последние действия. Для этого достаточно выбрать команду меню Edit/Undo (Правка/Отменить) столько раз, сколько последних операций требуется отменить. Команда меню Edit/Redo (Правка/Вернуть) возвращает отмененное действие. Можно также воспользоваться кнопками Undo (Оменить) и Redo (Вернуть) на стандартной панели инструментов. Треугольники справа от кнопок служат для вызова списка последних действий.<br />
Кроме того, в CorelDRAW можно отказаться от всех действий, которые были произведены после открытия файла. Для этого следует воспользоваться командой меню File/Revert (Файл/Восстановить). Откроется диалоговое окно, в котором будет предложено подтвердить восстановление. Нажатие на кнопку ОК безвозвратно удалит все изменения, произведенные после последнего сохранения файла.<br />
 Перемещение, копирование и удаление объектов. Удаление объектов. Чтобы удалить один или несколько объектов, проще всего выделить требуемые объекты и нажать клавишу [Delete]. Кроме того, можно для удаления выделенных объектов можно использовать команду меню Edit/Delete (Правка/Удалить) или команду контекстного меню Delete (Удалить).<br />
Перемещение объектов. Перемещать объекты в CorelDRAW можно как с помощью мыши, так и с помощью клавиатурного ввода параметров перемещения. Чтобы переместить объект мышью, следует выделить его, подвести к нему мышь, и после того, как указатель мыши изменится на перекрестие из стрелок, щелкнуть на объекте левой кнопкой мыши и, удерживая ее нажатой, переместить его. Для перемещения объектов в строго горизонтальном или вертикальном направле­нии следует во время перемещения нажать и удерживать клавишу [Ctrl].<br />
Кроме того, переместить объект можно с помощью клавиш. Для этого необходимо его выделить и, нажимая клавиши управления курсором ([←], [↑], [→] или [↓]), переместить его в требуемое место. Клавиши управления курсором перемещают объект с шагом, который можно задать в счетчике Nudge Offset (Шаг смещения). Этот счетчик становится доступным на панели свойств документа, т.е. при условии, что ни один объект не выделен (рис.3). Если при перемещении объекта с помощью клавиш управления курсором удерживать нажатой клавишу [Ctrl], то объект будет смещаться на расстояние, равное половине значения, указанного в этом счетчике. Если же удерживать нажатой клавишу [Shift], то шаг смещения будет равен удвоенному значению параметра Nudge Offset (Шаг смещения). По умолчанию это значение равно 1 дюйму.<br />
<img src="img/img-145.png" ><br />
Копирование объектов. Копировать объекты в CorelDRAW можно как с использованием буфера обмена, так и без него.<br />
Чтобы скопировать объекты, не помещая их в буфер, следует выделить эти объекты, а затем выбрать команду меню Edit/Duplicate (Правка/Дубликат) или нажать клавишу [+] справа от цифровой клавиатуры. Дубликаты объектов будут созданы на том же месте, где расположены оригиналы. Этот способ копирования объектов удобнее всего применять для копирования в пределах одной страницы документа.<br />
Если требуется скопировать объекты на другую страницу или в другой документ, то следует сначала выделить объекты и скопировать их в буфер, а затем вставить их из буфера в нужное место. Скопировать объекты в буфер можно одним из следующих способов: 1) выбрать команду меню Edit/Copy (Правка/Копировать); 2) щелкнуть на выделенных объектах правой кнопкой мыши и выбрать команду Copy (Копировать) в появившемся вспомогательном меню; 3) нажать кнопку Copy (Копировать) на стандартной панели инструментов; 4) нажать сочетание клавиш [Ctrl]+[Ins] или [Ctrl]+[С].<br />
После того, как объекты скопированы в буфер, вставить их можно одним из следующих способов: 1) выбрать команду меню Edit/Paste (Правка/Вставить); 2) щелкнуть правой кнопкой мыши на свободной области листа и в появившемся контекстном меню выбрать команду Paste (Вставить); 3) нажать кнопку Paste (Вставить) на стандартной панели инструментов; 4) нажать сочетание клавиш [Shift]+[Ins] или [Ctrl]+[V]. При этом оригинал объекта остается на своем прежнем месте.<br />
Третий способ. Этот способ отличается от предыдущего тем, что объекты копируются в буфер, и при этом удаляются с листа. Для этого используется команда меню Edit/Cut (Правка/Вырезать). Для вставки объекта из буфера можно использовать один из описанных выше способов.<br />
Четвертый способ. Все предыдущие способы используются для создания независимых копий объектов. Однако в CorelDRAW есть возможность создавать копии, которые связанны с оригиналом — клоны. Чтобы создать клон, необходимо выделить объекты и выбрать команду меню Edit/Clone (Правка/Клонировать). Созданный в результате этой операции объект полностью зависит от параметров оригинала: от типа заливки, контура и т.п., т.е. при изменении параметров оригинала будут меняться параметры копии-клона. При этом, если изменять свойства клона (например, заливку), то свойства оригинала останутся прежними, но при этом связь между оригиналом и клоном будет нарушена.<br />
Пятый способ. Если переместить объект, удерживая правую кнопку мыши, то появится контекстное меню, в котором можно выбрать одну из команд:<br />
- Move Here (Переместить сюда) — перемещает объект;<br />
-Copy Here (Скопировать сюда) — копирует объект.<br />
<b>СОСТАВНЫЕ ОБЪЕКТЫ</b><br />
В CorelDRAW широкое применение имеют операции, позволяющие получить составные объекты путем объединения, пересечения отдельных объектов, а также исключения одних объектов из других<br />
<img src="img/img-146.png" ><br />
<ol>
	<li>Объединение объектов. Для того, чтобы получить форму объединением двух и более объектов, можно воспользоваться одним из следующих способов:</li>
</ol>
1) выделить все объекты и нажать кнопку Weld (Объединение) на панели свойств;<br />
2) выделить нужные объекты и выбрать команду меню Arrange/Shaping/Weld (Упорядочить/Формирование/Объединение);<br />
3) выделить один или несколько объектов, выбрать команду меню Arrange/Shaping/Shaping (Упорядочить/Формирование/Формирование), в появившейся панели Shaping (Формирование) выбрать в выпадающем списке Weld (Объединение), щелкнуть на кнопке Weld To (Объединить с) и указать мышью на объекте, с которым требуется объединить выделенные.<br />
<img src="img/img-147.png" ><br >
При использовании последнего из описанных способов появляется возможность оставлять объекты, участвующие в операции объединения, на рабочей области, тогда как первые два способа оставляют только конечный результат.<br />
Выбрать, какие именно объекты будут оставлены, позволяют флажки Source Object (Исходный объект) и Target Object (Целевой объект). Если оставлять объекты-операнды не требуется, то порядок выделения не важен: результат операции будет одним и тем же вне зависимости от порядка выделения объектов. Если же требуется оставить какой-либо из объектов после выполнения операции, то порядок выделения помогает определить, какой объект исходный (выделен первым), а какой — целевой (выделен вторым).<br />
<ol>
	<li>Пересечение объектов. Для того, чтобы получить форму, являющуюся пересечением нескольких объектов, следует воспользоваться одним из следующих способов:</li>
</ol>
1) выделить все объекты и нажать кнопку Intersect (Пересечение) на панели свойств;<br />
2) выделить нужные объекты и выбрать команду меню Arrange/Shaping/Intersect (Упорядочить/Формирование/Пересечение);<br />
3) выделить один или несколько исходных объектов, выбрать команду меню Arrange/Shaping/Shaping (Упорядочить/Формирование/Формирование), на появившейся панели Shaping (Формирование) в выпадающем списке операций выбрать Intersect<br />
(Пересечение), нажать кнопку Intersect With (Пересечь с) и щелкнуть мышью на объекте-цели.<br />
Как и в случае с объединением форм, при использовании первых двух способов очередность выбора объектов не важна, тогда как при использовании третьего способа она может быть важна, если нужно указать, какие объекты после операции пересечения останутся, а какие будут удалены.<br />
<ol>
	<li>Исключение объектов. Для операции исключения объектов очередность их выделения важна: объекты, которые выделены первыми, будут исключаться из объектов, которые выделены последними.</li>
</ol>
Для того чтобы исключить, или, как еще говорят, вырезать один объект из другого, следует воспользоваться одним из следующих способов:<br />
1) выделить сначала объекты, которые будут исключаться, затем, в последнюю очередь, объект, из которого будет производиться исключение, и нажать кнопку Trim (Вырезка) на панели свойств;<br />
2) выделить по очереди объекты, участвующие в исключении, после чего выбрать команду меню Arrange/Shaping/Trim (Упорядочить/Формирование/Вырезка);<br />
3) выделить объекты, которые требуется исключить, выбрать команду меню Arrange/Shaping/Shaping (Упорядочить/Формирование/Формирование), в появившейся панели выбрать в раскрывающемся списке операцию Trim (Вырезка), установить с помощью соответствующих флажков, какие из объектов-операндов останутся, а какие будут удалены, после чего нажать кнопку Trim (Вырезать) и щелкнуть мышью на целевом объекте.<br />
Кроме того, имеются дополнительные возможности исключения объектов с помощью команд, которые можно выполнить, нажав соответствующие кнопки на панели свойств, или выбрав соответствующие команды в меню Arrange/Shaping (Упорядочить/Формирование): Simplify (Упрощенно), Front Minus Back (Передний без заднего) и Back Minus Front (Задний без переднего). С их использованием ознакомьтесь самостоятельно.<br />
<b>Модель кривой</b><br />
<b> </b><br />
 В основе принятой в Corel Draw модели линии лежат два понятия: узел и сегмент.<br />
Узел – точка на плоскости изображения, фиксирующая положение одного из концов сегмента кривой.<br />
Сегментом называется часть линии, соединяющая два смежных узла.<br />
Узлы и сегменты неразрывно связаны друг с другом: в замкнутой линии узлов столько, сколько сегментов, в незамкнутой – на один больше (рис.1).<br />
