Режимы отображения документа. CorelDRAW имеет пять режимов отображения документа (рис.5):<br />
- Simple Wireframe (Упрощенный каркас) — режим отображения каркаса иллюстрации, при котором отображаются только контуры иллюстраций; очень удобен для точного размещения контуров объектов в сложных иллюстрациях; кроме того, в этом режиме несколько упрощено отображение некоторых эффектов (смешения, экструзии и контура);<br />
- Wireframe (Каркас) — аналогичен предыдущему, но эффекты отображаются полностью;<br />
- Draft (Черновик) — используется преимущественно для компоновки элементов макета: растровые изображения отображаются с пониженным разрешением, а цвета заливок рассчитываются приблизительно; при этом увеличивается скорость прорисовки изображения;<br />
- Normal (Обычный) — используется в большинстве случаев, поскольку представляет реальный компромисс между качеством и скоростью отображения;<br />
- Enhanced (Улучшенный) — использует максимальное разрешение растровых изображений, сглаживание контуров объектов и шрифтов, обеспечивая максимальное качество демонстрации документа; однако, скорость прорисовки документа снижается.<br />
