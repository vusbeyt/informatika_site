Для изменения масштаба следует воспользоваться инструментом Zoom Tool (Масштаб) панели Toolbox (Инструменты). После выбора этого инструмента указатель мыши примет вид увеличительного стекла со знаком «плюс». Щелчок левой кнопкой мыши приведет к увеличению масштаба, правой — к уменьшению.<br />
Другой, более точный, способ увеличения масштаба изображения состоит в следующем: выбрать инструмент Zoom Tool (Масштаб) и обвести рамкой выделения ту область, которую необходимо рассмотреть.<br />
Кроме того, при выборе инструмента &quot;Zoom Tool&quot; на панели Property Bar (Панель свойств) появляются следующие кнопки, дающие дополнительные возможности по изменению масштаба изображения:<br />
- Zoom In (Увеличить масштаб) — увеличивает масштаб изображения;<br />
- Zoom Out (Уменьшить масштаб) — уменьшает масштаб изображения;<br />
- Zoom To Selected (Масштаб по выделенному) — изменяет масштаб выделенной области;<br />
- Zoom To All Object (Масштаб по всем объекту) — позволяет установить масштаб изображения, при котором будут видны все объекты сразу;<br />
- Zoom 1:1 (1:1) — отображает рисунок в масштабе 1:1;<br />
- Zoom To Page (Масштаб по странице) — устанавливает отображение всей страницы;<br />
- Zoom To Page Width (Масштаб по ширине страницы), Zoom To Page Height (Масштаб по высоте страницы) — отображают лист по ширине или по высоте соответственно.<br />
С помощью инструмента Hand Tool (Рука) можно передвигаться по изображению. Кроме того, панорамирование изображения можно осуществлять с помощью полос прокрутки, с помощью квадратного значка, расположенного на пересечении полос прокрутки, а также нажав и удерживая кнопку-колесико мыши.<br />
Для изменения масштаба просмотра можно использовать выпадающий список масштабов, расположенный в правой части панели Standard (Основная), а также прокручивая кнопку-колесико мыши.<br />
