<b>СТРАНИЦЫ В CorelDRAW</b><br />
CorelDRAW позволяет создавать многостраничные макеты.<br />
Переименование страниц. Изначально каждый новый документ содержит всего одну страницу, которая называется Page 1. Чтобы переименовать страницу, можно щелкнуть правой кнопкой мыши на ярлычке с ее именем в нижней части окна, слева от горизонтальной полосы прокрутки, в появившемся меню выбрать команду Rename Page (Переименовать страницу), в открывшемся диалоговом окне в поле Page Name (Имя страницы) ввести имя и нажать кнопку ОК. Кроме того, можно воспользоваться командой меню Layout/Rename Page (Макет/Переименовать страницу).<br />
Добавление и удаление страниц. Чтобы вставить страницу перед или после текущей, можно воспользоваться одной из кнопок слева от вкладок с именами страниц. Однако эта кнопка действует только в том случае, если активной является первая или последняя страница, и вставляет только одну страницу. Если же необходимо добавить несколько страниц, то удобнее воспользоваться командой меню Layout/Insert Page (Макет/Вставить страницу): появится диалоговое окно, в котором можно установить сколько страниц нужно вставить до (Before) или после (After) текущей страницы (Page), будут ли они ориентированы вертикально (Portrait) или горизонтально (Landscape), а также формат и размеры бумаги.<br />
 Наконец, можно щелкнуть правой кнопкой на ярлычке с именем одной из страниц и в появившемся контекстном меню выбрать команду Insert Page After (Вставить страницу после) или Insert Page Before (Вставить страницу до).<br />
 Чтобы удалить страницу, можно воспользоваться командой Delete Page (Удалить страницу) контекстного меню, которое вызывается правым щелчком мыши на ярлычке с ее именем. Кроме того, можно удалить сразу несколько страниц, выбрав команду меню Layout/Delete Page (Макет/Удалить страницу). В поле Delete Page (Удалить страницу) следует ввести номер страницы, которую нужно удалить. Если же требуется удалить несколько страниц, начиная с некоторой, следует установить флажок Through to page (До страницы) и в поле справа от него ввести, до какой страницы включительно удалить, начиная со страницы, указанной в поле Delete Page (Удалить страницу).<br />
Навигация по страницам. Слева от ярлычков страниц расположены кнопки навигации: — перейти к первой странице, — перейти к предыдущей странице, — перейти к последней странице, — перейти к следующей странице. Если требуется перейти к какой-либо определенной странице, то удобно воспользоваться командой меню Layout/Go To Page (Макет/Перейти к странице). Кроме того, для быстрой навигации по страницам можно использовать клавиши [Page Up] и [Page Down] или попросту щелкать на ярлычке с именем нужной страницы.<br />
Ориентация и размеры страниц. Страницы одного документа могут иметь разные размеры и ориентацию. Для изменения ориентации существует несколько способов: 1) нажать кнопку Portrait (Портретная) или Landscape (Альбомная) на панели свойств инструмента Pick Tool (Стрелка) при условии, что ни один объект не выделен; 2) щелкнуть на ярлычке страницы правой кнопкой мыши и выбрать в контекстном меню<br />
команду Switch Page Orientation (Изменить ориентацию страницы); 3) выбрать команду меню Layout/Switch Page Orientation (Макет/Изме­нить ориентацию страницы); 4) выбрать команду меню Layout/Page Setup (Макет/Параметры страницы) и в появившемся диалоговом окне Options (Параметры) установить ориентацию страницы с помощью переключателя Portrait/Landscape (Портретная/Альбомная).<br />
Размеры страницы можно установить в полях Paper Width and Height (Ширина и высота бумаги) панели свойств инструмента Pick Tool (Стрелка) при отсутствии выделенных объектов, а также с помощью диалогового окна Options (Параметры).<br />
Следует отметить, что это диалоговое окно позволяет также устанавливать, будут ли настройки размеров страницы применяться только к текущей странице (Resize current page only), подобрать подходящий формат страницы из установок принтера (Set From Printer), а также добавить к иллюстрации прямоугольник с размерами, равными установленным размерам страницы (Add Page Frame).<br />
Фон страницы. По умолчанию страницы белые, т.е. не имеют никакого фонового цвета или рисунка. Если же требуется изменить фон страницы, то следует воспользоваться командой меню Layout/Page Background (Макет/Фон страницы). На экране появится диалоговое окно, в котором переключатель фона по умолчанию установлен в положение No Background (Нет фона). Если требуется изменить только цвет фона, то следует установить этот переключатель в положение Solid (Сплошной) и в появившемся списке цветов выбрать нужный цвет.<br />
В некоторых случаях в качестве фона требуется использовать рисунок. Тогда следует установить переключатель в положение Bitmap (Растровый), после чего нажать кнопку Browse (Обзор). Появится стандартное диалоговое окно импорта файлов, в котором требуется выбрать файл с растровым форматом, который требуется использовать в качестве фона и нажать кнопку Import (Импорт). После этого в разделе Source (Источник) будет указано место расположения файла, а с помощью соответствующего переключателя можно установить, будет ли рисунок внедрен в документ (Embedded) или будет ли вставлен как ссылка на ресурс (Linked). В разделе Bitmap Size (Размер растра) можно установить, что будет использоваться исходный (Default Size) или пользовательский (Custom Size) размер рисунка; во втором случае можно будет установить его размеры по высоте и ширине.<br />
<b>Специальные эффекты </b><br />
<strong>Одной из мощнейших возможностей программы Corel Draw 13 (X3), разработанной в Corel Corporation справедливо считается возможность применять к объектам всевозможные художественные эффекты Corel Draw, позволяющие создавать удивительные по красоте иллюстрации, например, эффекты тени, прозрачности, линзы, перспективы и др. Наши уроки Corel Draw расскажут о настройке параметров этих эффектов как путем ввода конкретных числовых значений, так и интерактивно. Обучение Corel эффектам я разбил на несколько уроков, получив, таким образом, для программы Corel Draw учебник по спецэффектам.</strong>
Для применения специальных эффектов служат инструменты, объединенные на специальной панели в разделе Toolbox (Инструменты)<br />
<strong>Corel Draw: эффект Blend (Смешение)</strong>
Этот эффект позволяет создавать цепочку объектов, преобразовывающих один объект в другой путем прогрессии формы и цветов (рис.1). При этом смешение форм может проходить как вдоль прямой, так и вдоль произвольной кривой, а также может быть составным. В создании этого эффекта участвуют два объекта: Start (Стартовый) и End (Конечный).<br />
<img src="img/img-163.png" ><br />
преобразования круга с однородной заливкой в цветок с градиентной.<br />
Чтобы создать эффект смешения вдоль прямой, на панели инструментов следует выбрать инструмент <strong>Interactive Blend Tool (Интерактивный смеситель)</strong> (рис.2). Затем следует щелкнуть на одном из объектов и, удерживая кнопку мыши нажатой, переместить курсор на второй объект и отпустить кнопку мыши: между стартовым и конечным объектами появится цепочка промежуточных объектов.<br> Чтобы создать эффект смешения вдоль произвольной кривой, можно, удерживая клавишу [Alt], рисовать произвольную кривую, соединяющую стартовый и конечный объекты. Эта кривая будет отображаться в виде синей пунктирной линии.<br />
В обоих случаях, объект, порядок которого был ниже (см. предыдущие уроки corel draw на нашем сайте), станет стартовым объектом, а тот объект, порядок которого был выше - конечным.<br />
<img src="img/img-164.png" ><br />
При использовании эффекта смешения необходимо помнить следующее:<br />
- если перемещать стартовый или конечный объекты, то цепочка промежуточных объектов будет также перемещаться;<br />
- если удалить стартовый или конечный объект, будет удалена и цепочка переходных объектов;<br />
- если изменять цвет или форму стартового или конечного объекта, то будут меняться цвет и форма промежуточных объектов;<br />
- цветовая прогрессия будет иметь место только в том случае, если объекты имеют либо однородную, либо градиентную заливку.<br />
Рассмотрим теперь панель свойств инструмента Interactive Blend Tool ((Интерактивный смеситель).<br />
В выпадающем списке Preset List (Список заготовок) можно выбрать одну из уже созданных ранее заготовок этого эффекта, а с помощью кнопок Add Preset (Добавить заготовку) и Delete Preset (Удалить заготовку) можно добавлять свои заготовки в список и удалять их из списка.<br />
С помощью кнопок Use Steps or Fixed Spacing for Blend (Использовать шаги или фиксированное расстояние) можно установить, будет ли задаваться положение промежуточных форм их числом или расстоянием между ними. В полях раздела Number of Steps or Offset Between Blend Shapes (Число шагов или расстояние между формами) можно установить, соответственно, количество промежуточных форм или расстояние между ними.<br />
В поле Blend Direction (Направление смешения) можно установить, на сколько градусов будет повернута каждая следующая фигура относительно предыдущей. С помощью кнопок Direct Blend (Направленное смешение), Clockwise Blend (Смешение по часовой стрелке) и Counterclockwise Blend (Смешение против часовой стрелки) можно установить, каким образом в процессе смешения будет прогрессировать цвет относительно спектра.<br />
Щелчок по кнопке Object and Color Acceleration (Ускорение объекта и цвета) активизирует панель, на которой можно установить, будет ли изменение формы и цвета объектов ускоряться при приближении к стартовому или конечному объекту. По умолчанию ускорение изменения цвета и ускорение изменения формы происходят одинаково. Это обеспечивается нажатой кнопкой с замочком на этой панели. Если кнопку отжать, то регулировать ускорения можно будет независимо друг от друга. Кроме того, для интерактивной регулировки этих ускорений предназначены треугольные маркеры на пунктирной линии-пути смешения.<br />
Кнопка Start and End Objects Properties (Свойства стартового и конечного объектов) вызывает вспомогательное меню, содержащее следующие команды:<br />
 - New Start (Новый стартовый) - позволяет выбрать другой объект в качестве стартового для текущего смешения; при этом порядок нового стартового объекта не может быть выше порядка текущего конечного объекта;<br />
- Show Start (Показать стартовый) - выделяет текущий стартовый объект;<br />
- New End (Новый конечный) - позволяет выбрать другой объект в качестве конечного для текущего смешения; при этом порядок нового конечного объекта не может быть ниже порядка текущего стартового объекта;<br />
- Show End (Показать конечный) - выделяет текущий конечный объект.<br />
Кнопка Path Properties (Параметры пути) программы Corel Draw активизирует вспомогательное меню, которое содержит следующие команды:<br />
- New Path (Новый путь) - позволяет выбрать некоторую кривую в качестве пути для эффекта смешения; при этом стартовый и конечный объекты будут перемещаться только вдоль этой кривой-пути; если же требуется чтобы стартовый и конечный объекты расположились точно на концах этой кривой, то можно нажать на панели свойств кнопку Miscellaneous Blend Options (Различные параметры смешения) и на появившейся панели установить флажок Blend Along Full Path (Смешение вдоль всего пути);<br />
- Show Path (Показать путь) - выделяет текущую линию-путь;<br />
- Detach From Path (Отсоединить от пути) - отсоединяет объекты смешения от текущего пути.<br />
Все вышеописанные настройки эффекта смешения становятся доступными на соответствующей панели, которая активизируется, если выбрать команду меню <strong>Effects/Blend (Эффекты/Смешение).</strong><br />

<strong>Corel Draw 13: эффект Contour (Котнур)</strong>
Эффект Contour (Контур) предназначен для многократного оконтуривания объектов, направленного как внутрь объекта, так и наружу (рис.3).<br />
Применить этот эффект можно с помощью команды меню <strong>Effect/Contour (Эффект/Контур)</strong> или с помощью инструмента Interactive Contour Tool (Интерактивный контур) на панели Toolbox (Инструменты). В последнем случае нужно выделить объект, затем выбрать инструмент Interactive Contour Tool (Интерактивный контур) щелкнуть на объекте, не отпуская мышь, переместить курсор внутрь объекта или наружу, после чего щелкнуть еще раз мышью.<br />
После применения эффекта появится специальный управляющий вектор с маркерами на концах и ползунком. Длина вектора соответствует ширине контура, а перемещение маркеров позволяет регулировать ширину и направление контура. При этом при увеличении ширины контура увеличивается число фигур контура, но не их ширина. Ширину контурных фигур можно изменять, передвигая ползунок на управляющем векторе.<br />
<img src="img/img-165.png" ><br />
Продолжим <strong>обучение Corel</strong>. Рассмотрим панель свойств инструмента Interactive Contour Tool (Интерактивный контур). В списке Presets (Заготовки) предлагается несколько уже настроенных эффектов контура. С помощью кнопок Add Preset (Добавить заготовку) и Delete Preset (Удалить заготовку).<br />
Кнопки To Center (К центру), Inside (Внутрь) и Outside (Наружу) устанавливают тип оконтуривания. В поле Contour Steps (Шаги контура) следует установить число контурных фигур. Следует отметить, что это поле недоступно при работе с типом эффекта To Center (К центру): в этом случае число фигур подбирается таким образом, чтобы они заполнили весь объект. В поле Contour Offset (Смещение контура) можно задать ширину контурных фигур.<br />
С помощью кнопок Linear Contour Colors (Цвета контура линейно), Clockwise Contour Colors (Цвета по часовой стрелке) и Counterclockwise Contour Colors (Цвета против часовой стрелки) можно установить направление перехода цветов объекта к цветам заливки и контура конечной контурной фигуры, которые, в свою очередь, устанавливаются в полях Outline Color (Цвет контура) и Fill Color (Цвет заливки).<br />
С помощью кнопки Object and Color Acceleration (Ускорение объекта и цвета) активизируется панель, на которой с помощью соответствующих ползунков можно устанавливать, с каким ускорением будут изменяться ширина и цвет контурных фигур.<br />
<strong>Эффект Corel Draw: Distortion (Искажение)</strong>
Интересные и порой неожиданные изображения можно получить, применяя в программе, производства <strong>Corel Corparation</strong> к объектам эффект искажения. Инструмент для создания этого эффекта - <strong>Interactive Distortion Tool</strong> (Интерактивное искажение) - также расположен на панели Toolbox (Инструменты). Для того чтобы применить эффект искажения, нужно выделить объект, выбрать этот инструмент, щелкнуть на объекте и, не отпуская кнопку мыши, протянуть вектор искажения.<br />
Для <strong>эффектов CorelDRAW </strong>используются три основных типа искажений: Push and Pull Distortion (Втягивание и вытягивание), Zipper Distortion («Молния») и Twister Distortion (Скручивание), что отражают наши уроки corel draw (рис.4). Для выбора типа искажения служат специальные кнопки на панели свойства инструмента Interactive Distortion Tool (Интерактивное искажение). Примечательно, что к уже искаженному объекту можно применять новые искажения. Однако, этот эффект очень требователен к ресурсам компьютера, поэтому сильно увлекаться многоразовым применением его к объектам не стоит.<br />
<img src="img/img-166.png" ><br />
Какой бы тип искажения не был выбран, на панели свойств будут расположены следующие четыре кнопки:<br />
- Center Distortion (Искажение от центра) - перемещает центр искажения в геометрический центр объекта;<br />
- Convert To Curves (Преобразовать в кривые) - преобразовывает искаженный объект в обычную кривую;<br />
- Copy Distortion Properties (Копировать свойства искажения) - позволяет скопировать параметры искажения с другого объекта: по нажатию на кнопку курсор изменится на широкую черную стрелку, после чего можно указать, параметры искажения какого объекта требуется скопировать;<br />
- Clear Distortion (Отменить искажение) - преобразовывает объект к изначальному, неискаженному виду.<br />
Помимо этого, каждому из типов искажения присущи свои специфические свойства, поля для настройки которых появляются в соответствии с тем, какой из типов выбран:<br />
 <strong>Эффекты Corel Draw: Втягивание и вытягивание</strong>. Этому типу искажения присуще только одно специфическое свойство - Push and Pull Distortion Amplitude (Амплитуда втягивания/вытягивания): положительные значения этого параметры соответствуют втягиванию, отрицательные - вытягиванию.<br />
 <strong>Эффекты Corel Draw: «Молния»</strong>. Для настройки этого типа искажения в CorelDRAW предусмотрены следующие параметры: 1) амплитуда (Zipper Distortion Amplitude) - величина, пропорциональная длине вектора; для ее изменения можно также использовать квадратный маркер на конце управляющего вектора; 2) частота (Zipper Distortion Frequency) - для ее изменения также служит ползунок на управляющем векторе.<br />
Кроме того, c помощью специальных кнопок на панели свойств можно задать дополнительные атрибуты этого типа искажения: хаотичность (Random Distortion), сглажен-ность (Smooth Distortion) и локальность (Local Distortion).<br />
<strong>Эффекты Corel Draw: Скручивание</strong>. Для этого типа специфическими параметрами являются: 1) направление скручивания - по часовой стрелке (Clockwise Rotate) или против часовой стрелки (Counterclockwise Rotate); 2) число полных оборотов (Complete Rotations); 3) дополнительное вращение (Additional Degrees) - для интерактивного изменения служит круглый маркер на управляющем векторе.<br />
