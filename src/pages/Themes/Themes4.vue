<b>ОСНОВЫ РАБОТЫ С ТЕКСТОМ</b><br />
CorelDRAW существует две разновидности текстовых объектов: фигурный, или художественный (Artistic) и обычный, или простой (Paragraph) текст. Панель Property Bar (Панель свойств) при работе с текстовыми объектами похожа на панель форматирования текстового редактора.<br />
<img src="img/img-156.png" ><br />
Фигурный текст представляет собой графический объект, с которым можно работать, как с любым другим объектом CorelDRAW. Обычный текст представляет собой массив текста в рамке. Можно менять границы рамки обычного текста или придавать ей замысловатую форму, но внутри текст будет располагаться точно так же, как и в любом текстовом редакторе, например, в Microsoft Word.<br />
Фигурный текст, как правило, используется для заголовков и надписей. Обычный текст предназначен для ввода больших объемов текстовой информации и часто используется при создании рекламных листовок, а также при верстке в CorelDRAW газет и журналов.<br />
Фигурный текст. Чтобы добавить фигурный текст, нужно выбрать инструмент Text Tool (Текст) в панели инструментов Toolbox (Инструменты), установить указатель мыши на том месте, где требуется ввести текст и щелкнуть мышью: на месте щелчка появится текстовый курсор, который указывает, куда будет вставлен следующий символ при вводе с клавиатуры. На панели Property Bar (Панель свойств) можно установить гарнитуру шрифта, его размер, начертание, выравнивание текста. После этого можно вводить текст, причем, по завершении ввода вокруг текста появятся маркеры выделения, позволяющие работать с текстом, как с любым другом объектом.<br />
Если выделен текстовый объект целиком, изменится форматирование всех символов. При выбранном инструменте Text Tool (Текст) можно выделить часть текста, и поменять только его форматирование. В списках панели Property Bar (Панель свойств) настраиваются основные свойства текстового объекта. Дополнительные возможности по форматированию текста можно получить в специальном диалоге, который вызывается с помощью кнопки Format Text (Форматировать текст) панели Property Bar (Панель свойств).<br />
Чтобы отобразить все невидимые символы, достаточно нажать кнопку Non printing Characters (Непечатаемые символы). При печати эти символы будут не видны, поскольку они предназначены только для удобства при редактировании и оформлении текста.<br />
Редактирование текста осуществляется также просто. При выбранном инструменте Text Tool (Текст) следует щелкнуть мышью перед любым символом в тексте, и на месте щелчка появится текстовый курсор. В этом режиме можно перемещать текстовый курсор по фигурному тексту с помощью клавиш управления курсором. Удалять символы можно с помощью клавиш [Delete] или [Backspace]. При вводе фигурного текста, если требуется перейти на новую строку, следует воспользоваться клавишей [Enter]. Как и в текстовом редакторе, выделять фрагменты и перемещать курсор можно как с помощью мыши, так и с помощью специальных сочетаний клавиш.<br />
Обычный текст. Перед вводом обычного текста необходимо определить рамку, в которой он будет размещен. Чтобы задать эту рамку, нужно подвести указатель мыши к верхнему левому краю области, в которой требуется разместить текст, нажать левую кнопку мыши, и, не отпуская ее, начать передвигать мышь: на экране появится прямоугольная пунктирная рамка, размеры которой меняются вместе с передвижениями мыши. Далее следует подобрать нужный размер пунктирной рамки и отпустить кнопку мыши: рамка останется на экране, в ее начале появится текстовый курсор, а вокруг — маркеры выделения. На панели свойств можно выбрать параметры текста. После того, как текст будет введен, он будет отформатирован выбранным способом.<br />
При работе с обычным текстом добавляется несколько дополнительных возможностей по изменению формата. Кнопки Decrease Indent (Уменьшить отступ) и  Increase Indent (Увеличить отступ) панели свойств позволяют уменьшить и увеличить отступ текста от края рамки. Нажатие кнопки Show/Hide Drop Cap (Показать/Скрыть буквицу) панели свойств позволит получить эффект буквицы, а нажатие кнопки Show/Hide Bullet (Показать/Скрыть список) добавит маркеры списка к выделенным абзацам.<br />
