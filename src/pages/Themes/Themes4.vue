Узлы линии отображаются на экране в виде небольших квадратиков с закругленными углами. Начальный узел отображается квадратиком чуть большего размера.<br />
Инструменты, позволяющие строить линии различных типов, сведены в CorelDraw в одну раскрывающуюся панель инструментов <b>Кривая </b>(рис.2).<br />
