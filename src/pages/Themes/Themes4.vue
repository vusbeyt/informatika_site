Инструментом "Выделение" устанавливаем дополнительную горизонтальную направляющую по верхним точкам объекта (до появления слова "узел").<br />
<img src="img/img-279.png" ><br />
Инструментом "Выделение" устанавливаем дополнительную вертикальную направляющую 85 мм.<br />
<img src="img/img-280.png" ><br />
Инструментом "Прямоугольник" рисуем прямоугольник слева от объекта "средняя грань".<br />
<img src="img/img-281.png" ><br />
Пока он выделен сразу преобразуем его в кривые - меню "Упорядочить" - "Преобразовать в кривую".<br />
<img src="img/img-282.png" ><br />
Инструментом "Форма" ("Фигура")<br />
<img src="img/img-283.png" ><br />
выделяем один из верхних углов прямоугольника<br />
<img src="img/img-284.png" ><br />
и делаем верхнюю сторону выгнутой.<br />
<img src="img/img-285.png" ><br />
Сторона еще (смотрится как) прямая, но у нее уже появились глифы (стрелки), которыми ее можно искривить.<br />
Берем эту сторону ЛКМ (левой кнопкой мыши) посередине (в точке пересечения с направляющей) и выгибаем ее вверх по направляющей.<br />
<img src="img/img-286.png" ><br />
В ДО этот объект называем "лв" ("левая грань").<br />
<img src="img/img-287.png" ><br />
Поскольку головка болта симметрична относительно центральной оси (направляющей) правую грань проще сделать так:<br />
· Скопировать левую грань.<br />
· И перенести копию направо, чтобы ее левая сторона шла вдоль вертикальной направляющей 110 мм.<br />
Копируем левую грань и тут же вставляем. Выделяем (инструментом &quot;Выделение&quot;) копию, переходим в докер &quot;Преобразования&quot; на вкладку &quot;Положение&quot;. В макете габарита устанавливаем галочку по левой стороне и в строке &quot;По горизонтали&quot; (верхняя) устанавливаем положение 110 мм.<br />
<img src="img/img-288.png" ><br />
Правая грань встала точно по месту.<br />
<img src="img/img-289.png" ><br />
В ДО этот объект называем "пр" ("правая грань").<br />
<img src="img/img-290.png" ><br />
Инструментом "Выделение" устанавливаем вертикальную направляющую по середине "правой грани".<br />
<img src="img/img-291.png" ><br />
И инструментом "Прямоугольник" рисуем прямоугольник по направляющим.<br />
<img src="img/img-292.png" ><br />
Здесь важно, что рисовать прямоугольник надо с верхнего левого угла (или правого верхнего), а нижняя сторона произвольна (в пределах высоты граней).<br />
В ДО этот прямоугольник называем "фаска" и помещаем его на самый низ.<br />
<img src="img/img-293.png" ><br />
Все объекты &quot;головки болта&quot; у нас готовы. Красим их. Красить будем (не мудрствуя лукаво) оттенками черного (в палитре вверху 10 оттенков). Предположим, свет падает справа сверху (я обычно беру такое освещение). Тогда распределение оттенков (по формуле тень - цвет - блик) будет следующее (от темного к светлому):<br />
· Левая грань - тень2. (Чем больше номер тени, тем она темнее).<br />
· Центральная грань - тень1.<br />
· Правая грань - цвет (в данном случае следующий по порядку более светлый оттенок).<br />
· Фаска - блик1. (Чем больше номер блика, тем он светлее. В данном случае у нас один блик).<br />
В ДО выбираем объект &quot;лв&quot; и красим его К50. Все черные оттенки в системе CMYK различаются разным количеством черного, т.е. К. Поэтому вполне достаточно указать только его. Чтобы увидеть состав оттенка надо навести курсор на цвет и немного подержать - появится рамочка с составом CMYK.<br />
Потом выбираем следующий по списку объект и заливаем следующим (более светлым) оттенком. И т.д.<br />
В ДО все объекты группируем, называем &quot;гол_болт&quot; (&quot;головка болта&quot;).<br />
<img src="img/img-294.png" ><br />
Удаляем обводки - щелкаем ПКМ (правая кнопка мыши) по ячейке с крестиком вверху палитры.<br />
<img src="img/img-295.png" ><br />
"Головка болта" готова.<br />
<img src="img/img-296.png" ><br />
Удаляем лишние (не нужные теперь) направляющие.<br />
<img src="img/img-297.png" ><br />
<b>Рисуем &quot;ножку болта&quot;.</b><br />
Инструментом &quot;Выделение&quot; выставляем две горизонтальные направляющие: 150 и 95 мм.<br />
<img src="img/img-298.png" ><br />
Инструментом "Прямоугольник" рисуем верхнюю часть "ножки" и красим ее линейной интерактивной заливкой (оттенки слева направо К90 - К20 - К50).<br />
<img src="img/img-299.png" ><br />
В ДО эту часть &quot;ножки&quot; называем &quot;врх&quot; (&quot;верх&quot;). Нижняя часть &quot;ножки&quot; будет &quot;резьба&quot;.<br />
<b>Рисуем &quot;резьбу&quot;.</b><br />
Резьба состоит из:<br />
· Цилиндра - &quot;внутреннего диаметра резьбы&quot; и<br />
· витков резьбы, которые накручиваются на цилиндр снаружи.<br />
Рисовать резьбу будем на другой странице. Для этого внизу в счетчике страниц создаем новую страницу и переходим на нее.<br />
Инструментом &quot;Выделение&quot; добавляем еще две вертикальные направляющие внутрь по 1 мм: 91 и 109 мм.<br />
<img src="img/img-300.png" ><br />
И по этим направляющим рисуем "внутренний диаметр резьбы" и красим его аналогично "верху", но (на один тон) темнее (К100 - К40 - К60).<br />
<img src="img/img-301.png" ><br />
В ДО называем его &quot;вну&quot; (&quot;внутренний диаметр резьбы&quot;).<br />
<b>Рисуем &quot;виток резьбы&quot;.</b><br />
К нижней горизонтальной направляющей добавляем еще три направляющие (через 1 мм): 94, 96 и 97 мм.<br />
<img src="img/img-302.png" ><br />
По внешним вертикальным направляющим инструментом "Кривая Безье" по точкам рисуем ломаную кривую.<br />
<img src="img/img-303.png" ><br />
Нажимаем кнопку F12 и переходим в докер &quot;Перо абриса&quot; (докер обводок). Задаем:<br />
· Толщину обводки 1 мм.<br />
· Цвет обводки - зеленый.<br />
· И обрезанные углы.<br />
<img src="img/img-304.png" ><br />
Нажимаем ОК.<br />
<img src="img/img-305.png" ><br />
Копируем и вставляем эту линию. Инструментом "Форма" удаляем крайние узы. Красим эту линию (остаток копии) в красный цвет.<br />
<img src="img/img-306.png" ><br />
Из меню "Упорядочить" - "Преобразовать абрис в объект" превращаем эту красную линию в объект и инструментом "Форма" добавляем на концах линии по 2 узла и правим по внешней направляющей и лежащей ниже зеленой линии.<br />
<img src="img/img-307.png" ><br />
<img src="img/img-308.png" ><br />
Так же и с другой стороны (зазубрину вниз). Зеленую линию удаляем. Красный отрезок называем в ДО "виток" и красим его интерактивной заливкой аналогично верхней части ножки (К90 - К20 - К50)<br />
<img src="img/img-309.png" ><br />
"Виток" должен быть немного светлее, чем "внутренний диаметр резьбы". Дополнительные (для построения) горизонтальные направляющие удаляем.<br />
Выделяем "виток" и из меню "Правка" вызываем докер "Шаг и повтор".<br />
<img src="img/img-310.png" ><br />
Устанавливаем в нем:<br />
•	Смещение по горизонтали - "без смещения".<br />
•	Смещение по вертикали - "смещение".<br />
•	И в нем "Расстояние" - 2 мм.<br />
•	Количество копий - 30.<br />
<img src="img/img-311.png" ><br />
Нажимаем "Применить".<br />
Получается 31 витков резьбы.<br />
<img src="img/img-312.png" ><br />
Докер "Шаг и повтор" закрываем.<br />
Смотрим на рисунок. Верхние три витка лишние. Их надо выделить в ДО и удалить. Верхний виток надо подправить инструментом "Форма", чтобы правая часть витка не вылезала за контур цилиндра.<br />
<img src="img/img-313.png" ><br />
Инструментом "Форма" подправляем по направляющим нижний виток.<br />
<img src="img/img-314.png" ><br />
<img src="img/img-315.png" ><br />
В ДО все "витки" группируем в группу "витки", располагаем эту группу выше объекта "вну" и, в свою очередь, группируем их. Называем эту группу "резьба".<br />
<img src="img/img-316.png" ><br />
И, наконец, на странице сборки (там, где у нас "головка болта") собираем все созданные объекты (группы):<br />
•	"резьба"<br />
•	"врх"<br />
•	"гол_болт"<br />
Группируем их в группу "болт".<br />
<img src="img/img-317.png" ><br />
В принципе, болт готов.<br />
<img src="img/img-318.png" ><br />
Но если присмотреться внимательно, то видно, что между отдельными объектами (группами): "головка болта" и "врх", "врх" и "резьба" видны волосяные линии. Особенно они заметны, например, на красном фоне.<br />
<img src="img/img-319.png" ><br />
Это незначительный, но все-таки брак изображения. Для того, чтобы от него избавиться, Надо под изображение подложить фон-подложку, контур которого совпадает с общим контуром изображения. И покрасить этот фон в какой-нибудь близкий изображению цвет (в нашем случае подойдет К50).<br />
Для этого копируем "болт" и переносим (вставляем) на другую (пустую) страницу. В ДО выделяем объект "витки" и удаляем их (чтобы у фона было не слишком много ненужных узлов). Остается такое изображение.<br />
<img src="img/img-320.png" ><br />
Выбираем в меню "Упорядочить" - "Формирование" - "Граница". Вокруг изображения появляется граница нового объекта.<br />
<img src="img/img-321.png" ><br />
Этот объект называем "фон", красим его К50,<br />
<img src="img/img-322.png" ><br />
переносим его на страницу сборки (где у нас болт с резьбой) и располагаем его ниж�� всех других объектов.<br />
<img src="img/img-323.png" ><br />
Вот теперь "болт" готов без всяких замечаний.<br />
<img src="img/img-324.png" ><br />
<b>Рисуем (соединяемые) пластины.</b><br />
Рисовать будем на новой пустой странице. (Можно оставить только страницу сборки, на которой &quot;болт&quot;. Остальные страницы промежуточных разработок болта удалить и снова сделать новую страницу.)<br />
Инструментом &quot;Выделение&quot; выставляем направляющие:<br />
· Горизонтальные - 170, 146 и 122 мм.<br />
· Вертикальные - 50, 89, 100, 111 и 150 мм.<br />
<img src="img/img-325.png" ><br />
Верхняя пластина состоит из собственно пластины и отверстия (вид сбоку). Рисуем их инструментом "Прямоугольник" (без всяких ухищрений) по направляющим.<br />
<img src="img/img-326.png" ><br />
В ДО объектов называем их "пл_врх" ("верхняя пластина") и "отв" ("отверстие"). Красим их:<br />
•	"пл_врх" - К60,<br />
•	"отв" - К80.<br />
Обводки удаляем и группируем в группу "пл_врх".<br />
<img src="img/img-327.png" ><br />
Точно также рисуем нижнюю пластину. Но красим чуть темнее:<br />
•	"пл_низ" - К70,<br />
•	"отв" - К90.<br />
Добавляем прямоугольник - общий фон. Помещаем его вниз и красим К80. Получаются такие пластины.<br />
<img src="img/img-328.png" ><br />
В ДО получается такая группа.<br />
<img src="img/img-329.png" ><br />
Всю эту группу переносим на страницу сборки (где болт) и размещаем ее ниже группы "болт".<br />
<img src="img/img-330.png" ><br />
Рисуем шайбу.<br />
Снова переходим на пустую страницу и выставляем направляющие:<br />
•	Горизонтальные - 122 (нижняя кромка нижней пластины) и 119 мм (толщина шайбы).<br />
•	Вертикальные - 78 и 122 мм (диаметр шайбы).<br />
<img src="img/img-331.png" ><br />
По пересечениям направляющих инструментом "Прямоугольник" рисуем прямоугольник, удаляем обводку, красим интерактивной заливкой (К90 - К40 - К70).<br />
<img src="img/img-332.png" ><br />
В ДО называем этот объект "шайба" и переносим его на страницу сборки. Помещаем выше всех других объектов (групп).<br />
<img src="img/img-333.png" ><br />
Рисуем гайку.<br />
Разметка гайки направляющими (на пустой странице).<br />
•	Горизонтальные - 119 (нижняя кромка шайбы) и 103 мм (толщина гайки).<br />
•	Вертикальные - 80, 90, 100 (центральная ось), 110 и 120 мм. (Вертикальные направляющие можно выставить по головке болта.)<br />
<img src="img/img-334.png" ><br />
Я рисовать гайку не буду, потому что она рисуется аналогично головке болта (см. выше).<br />
Разница лишь в том, что:<br />
•	У головки болта одна фаска (сверху), у гайки две (снизу и сверху).<br />
•	Раскраска гайки несколько темнее, чем головки болта (потому что она находится ниже).<br />
Должна получиться такая гайка.<br />
<img src="img/img-335.png" ><br />
Переносим ее на страницу сборки. Получается такая сборка.<br />
<img src="img/img-336.png" ><br />
Все объекты группируем в группу "болт".<br />
<img src="img/img-337.png" ><br />
Для завершения рисунка, инструментом "Прямоугольник" нарисуем прямоугольник 130 х100 мм. В ДО поместим его ниже группы "болт", назовем "фон", удалим обводку и закрасим в какой-нибудь светлый (бледный) цвет.<br />
<img src="img/img-338.png" ><br />
В ДО выделим группу "болт" и "фон" и нажмем на клавиатуре клавиши "Е" и "С". Это клавиши выравнивания выбранных объектов по горизонтали и вертикали.<br />
Рисунок принимает законченный вид.<br />
